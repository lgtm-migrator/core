export abstract class ControlCharacters {
  /**
   *
   *
   * @static
   * @memberof ControlCharacters
   */
  public static nl = '\x0a';

  /**
   *
   *
   * @static
   * @memberof ControlCharacters
   */
  public static nul = '\x00';

  /**
   *
   *
   * @static
   * @memberof ControlCharacters
   */
  public static bel = '\x07';

  /**
   *
   *
   * @static
   * @memberof ControlCharacters
   */
  public static bs = '\x08';

  /**
   *
   *
   * @static
   * @memberof ControlCharacters
   */
  public static ht = '\x09';

  /**
   *
   *
   * @static
   * @memberof ControlCharacters
   */
  public static np = '\x0c';

  /**
   *
   *
   * @static
   * @memberof ControlCharacters
   */
  public static cr = '\x0d';

  /**
   *
   *
   * @static
   * @memberof ControlCharacters
   */
  public static sp = '\x20';

  /**
   *
   *
   * @static
   * @memberof ControlCharacters
   */
  public static EOL = ControlCharacters.nl;
}
